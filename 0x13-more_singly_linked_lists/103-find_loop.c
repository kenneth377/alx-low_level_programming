#include "lists.h"

/**
 * find_listint_loop - finds the loop in a linked list using Floyd's Cycle Detection Algorithm. This algorithm involves two pointers moving through the linked list at different speeds, and if there is a loop, the two pointers will eventually meet.
 * @head: linked list to search for
 *
 * Return: address of the node where the loop starts, or NULL
 */
listint_t *find_listint_loop(listint_t *head)
{
	listint_t *slow = head;
	listint_t *fast = head;

	if (!head)
		return (NULL);

	while (slow && fast && fast->next)
	{
		fast = fast->next->next;
		slow = slow->next;
		if (fast == slow)
		{
			slow = head;
			while (slow != fast)
			{
				slow = slow->next;
				fast = fast->next;
			}
			return (fast);
		}
	}

	return (NULL);
}
